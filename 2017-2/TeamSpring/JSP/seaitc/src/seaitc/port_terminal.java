package seaitc;

public class port_terminal {
	//항구 터미널
	private String terminal_code;
	private String terminal_name;
	private String terminal_domi;
	private String terminal_tel;
	
	
	
	public String getTerminal_code() {
		return terminal_code;
	}
	public void setTerminal_code(String terminal_code) {
		this.terminal_code = terminal_code;
	}
	public String getTerminal_name() {
		return terminal_name;
	}
	public void setTerminal_name(String terminal_name) {
		this.terminal_name = terminal_name;
	}
	public String getTerminal_domi() {
		return terminal_domi;
	}
	public void setTerminal_domi(String terminal_domi) {
		this.terminal_domi = terminal_domi;
	}
	public String getTerminal_tel() {
		return terminal_tel;
	}
	public void setTerminal_tel(String terminal_tel) {
		this.terminal_tel = terminal_tel;
	}
}
