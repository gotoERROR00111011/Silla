package seaitc;

public class port_yard {
	private String working_yard;
	private String terminal_code;
	private int goals_working;
	private int goals_ship;
	private int present_working;
	private int present_ship;
	private String port_state;
	
	
	public String getWorking_yard() {
		return working_yard;
	}
	public void setWorking_yard(String working_yard) {
		this.working_yard = working_yard;
	}
	public String getTerminal_code() {
		return terminal_code;
	}
	public void setTerminal_code(String terminal_code) {
		this.terminal_code = terminal_code;
	}
	public int getGoals_working() {
		return goals_working;
	}
	public void setGoals_working(int goals_working) {
		this.goals_working = goals_working;
	}
	public int getGoals_ship() {
		return goals_ship;
	}
	public void setGoals_ship(int goals_ship) {
		this.goals_ship = goals_ship;
	}
	public int getPresent_working() {
		return present_working;
	}
	public void setPresent_working(int present_working) {
		this.present_working = present_working;
	}
	public int getPresent_ship() {
		return present_ship;
	}
	public void setPresent_ship(int present_ship) {
		this.present_ship = present_ship;
	}
	public String getPort_state() {
		return port_state;
	}
	public void setPort_state(String port_state) {
		this.port_state = port_state;
	}
}
